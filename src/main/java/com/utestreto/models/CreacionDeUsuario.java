package com.utestreto.models;

public class CreacionDeUsuario {

    private String nombre;
    private String apellido;
    private String correo;
    private String mes;
    private String dia;
    private String ano;
    private String pc;
    private String version_pc;
    private String idioma_pc;
    private String dispositivo;
    private String modelo_dispo;
    private String sistema_dispo;
    private String password;
    private String confirm_pass;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }

    public String getAno() {
        return ano;
    }

    public void setAno(String ano) {
        this.ano = ano;
    }

    public String getPc() {
        return pc;
    }

    public void setPc(String pc) {
        this.pc = pc;
    }

    public String getVersion_pc() {
        return version_pc;
    }

    public void setVersion_pc(String version_pc) {
        this.version_pc = version_pc;
    }

    public String getIdioma_pc() {
        return idioma_pc;
    }

    public void setIdioma_pc(String idioma_pc) {
        this.idioma_pc = idioma_pc;
    }

    public String getDispositivo() {
        return dispositivo;
    }

    public void setDispositivo(String dispositivo) {
        this.dispositivo = dispositivo;
    }

    public String getModelo_dispo() {
        return modelo_dispo;
    }

    public void setModelo_dispo(String modelo_dispo) {
        this.modelo_dispo = modelo_dispo;
    }

    public String getSistema_dispo() {
        return sistema_dispo;
    }

    public void setSistema_dispo(String sistema_dispo) {
        this.sistema_dispo = sistema_dispo;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getConfirm_pass() {
        return confirm_pass;
    }

    public void setConfirm_pass(String confirm_pass) {
        this.confirm_pass = confirm_pass;
    }
}
